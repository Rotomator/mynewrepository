package loopModule;

public class forEx {

	public static void main(String[] args) {

		for(int i=1; i<10; i++)
		{
			System.out.println(i);
			
		}

		//reverse
		for(int i=10; i>0; i--)
		{
			System.out.println(i);
			
		}
				
		//print table of 3 
		for(int i=1; i<=10; i++)
		{
			System.out.println(i + "*3=" + i*3);
			
		}
		for(int i=1; ;)
		{
			System.out.println(i + "*3=" + i*3);
			
		}
		
				
	}

}
